function addKeysToFile(filePath, newKeys, locale) {
  const raw = readFileSync(filePath, 'utf8')
  const json = JSON.parse(raw)

  if (!json.PartnerDashboard) {
    console.error(`${locale}: PartnerDashboard namespace not found!`)
    return
  }

  const existing = Object.keys(json.PartnerDashboard).length
  Object.assign(json.PartnerDashboard, newKeys)
  const after = Object.keys(json.PartnerDashboard).length

  writeFileSync(filePath, JSON.stringify(json, null, 2) + '\n', 'utf8')
  console.log(`${locale}: PartnerDashboard ${existing} -> ${after} keys (+${after - existing} new)`)
}

