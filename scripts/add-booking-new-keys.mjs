// Process each language file
const languages = ['en', 'es', 'fr']
let summary = []

for (const lang of languages) {
  const filePath = path.join(messagesDir, `${lang}.json`)

  try {
    const content = fs.readFileSync(filePath, 'utf-8')
    const messages = JSON.parse(content)

    // Add the PartnerBookingNew namespace
    if (!messages.PartnerBookingNew) {
      messages.PartnerBookingNew = {}
    }

    const newKeys = translations[lang]
    let addedCount = 0
    let skippedCount = 0

    for (const [key, value] of Object.entries(newKeys)) {
      if (messages.PartnerBookingNew[key] === undefined) {
        messages.PartnerBookingNew[key] = value
        addedCount++
      } else {
        skippedCount++
      }
    }

    // Write back with consistent formatting
    fs.writeFileSync(filePath, JSON.stringify(messages, null, 2) + '\n', 'utf-8')

    summary.push(`${lang}.json: Added ${addedCount} keys, skipped ${skippedCount} existing keys`)
  } catch (error) {
    summary.push(`${lang}.json: ERROR - ${error.message}`)
  }
}

console.log('\n=== PartnerBookingNew Translation Keys ===')
